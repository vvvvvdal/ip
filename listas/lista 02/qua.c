#include <stdio.h>

int main() {
    float m[2][2], r[2][2];

    int i, j;
    for(i = 0; i < 2; i++) {
        for(j = 0; j < 2; j++) {
            scanf("%f", &m[i][j]);
        }
    }
    
    r[0][0] = (m[0][0]*m[0][0]) + (m[0][1]*m[1][0]);
    r[0][1] = (m[0][0]*m[0][1]) + (m[0][1]*m[1][1]);
    r[1][0] = (m[1][0]*m[0][0]) + (m[1][1]*m[1][0]);
    r[1][1] = (m[1][0]*m[0][1]) + (m[1][1]*m[1][1]);


    printf("%.3f ", r[0][0]);
    printf("%.3f\n", r[0][1]);
    printf("%.3f ", r[1][0]);
    printf("%.3f\n", r[1][1]);

    return 0;
}